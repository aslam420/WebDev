const num = 5;

console.log(num + true);// true     1   // 6
console.log(num + false); // false  0   // 5
console.log(true + num);    // 6
console.log(false + num);   // 5

console.log(num - true);    // 4
console.log(num - false);   // 5
console.log(true - num);    // -4
console.log(false - num);   // -5